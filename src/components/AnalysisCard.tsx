type ScoreIndicatorProps = {
  score: number;
  labels: string[];
  max?: number;
};

export function ScoreIndicator({
  score,
  labels,
  max = 3,
}: ScoreIndicatorProps) {
  return (
    <div className="flex items-center gap-3">
      {/* star rating indicators */}
      <div className="flex gap-1">
        {[...Array(max)].map((_, i) => {
          const starValue = i + 1;
          const isFilled = starValue <= score;

          return (
            <svg
              key={i}
              className="h-6 w-6 transition-colors"
              viewBox="0 0 20 20"
              fill={isFilled ? '#1f2937' : 'white'}
              stroke="black"
              strokeWidth={1.5}
            >
              <path d="M9.049 2.927c.3-.921 1.603-.921 1.902 0l1.07 3.292a1 1 0 00.95.69h3.462c.969 0 1.371 1.24.588 1.81l-2.8 2.034a1 1 0 00-.364 1.118l1.07 3.292c.3.921-.755 1.688-1.54 1.118l-2.8-2.034a1 1 0 00-1.175 0l-2.8 2.034c-.784.57-1.838-.197-1.539-1.118l1.07-3.292a1 1 0 00-.364-1.118L2.98 8.72c-.783-.57-.38-1.81.588-1.81h3.461a1 1 0 00.951-.69l1.07-3.292z" />
            </svg>
          );
        })}
      </div>

      {/* score label */}
      <div className="flex items-center gap-2">
        <span className="text-sm font-semibold text-gray-900">
          {labels[score - 1]}
        </span>
        <span className="text-xs text-gray-500">
          ({score}/{max})
        </span>
      </div>
    </div>
  );
}

function QuoteBox({ quotes }: { quotes: string | null }) {
  if (!quotes) return null;

  const quoteList = quotes.split('\n').filter((q) => q.trim());

  return (
    <div className="mt-2 bg-gray-50 border border-gray-200 rounded p-3 space-y-2">
      <p className="text-xs font-medium text-gray-600">Supporting Evidence:</p>
      {quoteList.map((quote, idx) => (
        <p
          key={idx}
          className="text-xs text-gray-700 italic pl-3 border-l-2 border-gray-300"
        >
          &ldquo;{quote.replace(/^"|"$/g, '')}&rdquo;
        </p>
      ))}
    </div>
  );
}

